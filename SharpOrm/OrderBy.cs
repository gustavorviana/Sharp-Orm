namespace SharpOrm
{
    /// <summary>
    /// Specifies the order in which to sort the results of a query.
    /// </summary>
    public enum OrderBy
    {
        /// <summary>
        /// No specific order.
        /// </summary>
        None,
        /// <summary>
        /// Sort in ascending order.
        /// </summary>
        Asc,
        /// <summary>
        /// Sort in descending order.
        /// </summary>
        Desc
    }
}
