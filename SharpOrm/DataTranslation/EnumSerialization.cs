namespace SharpOrm.DataTranslation
{
    /// <summary>
    /// Specifies the serialization format for enums.
    /// </summary>
    public enum EnumSerialization
    {
        /// <summary>
        /// Serialize the enum as its numeric value.
        /// </summary>
        Value,

        /// <summary>
        /// Serialize the enum as its name.
        /// </summary>
        Name
    }
}
